#산술 연산자
print ("10 + 4 =", 10 + 4)
print ("10 - 4 =", 10 - 4)
print ("10 * 4 =", 10 * 4)
print ("10 / 4 =", 10 / 4)
print ("10 // 4 =", 10 // 4) #몫
print ("10 % 4 =", 10 % 4) #나머지

#도형의 면적
#1. 한변의 길이가 5인 정사각형의 높이
size = 5
area = size * size
print("한 변의 길이가 5인 정사각형의 넓이 :", area)

#2. 한변의 길이가 5이고 높이가 7인 삼각형의 넓이
w = 5
h = 7
area = (w * h) / 2
print ("길이가 5, 높이가 7인 삼각형의 넓이: ", area)
